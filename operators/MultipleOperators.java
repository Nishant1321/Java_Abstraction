
package operators;
import java.util.Scanner;

public class MultipleOperators {


	public static void main(String[] args) {
		// Arithmetic operators
		Scanner sc= new Scanner(System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a%b);
		System.out.println(a/b);
		

	}

}
